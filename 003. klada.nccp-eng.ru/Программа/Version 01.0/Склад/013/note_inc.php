				") or die(mysql_error());  // запрос на обновление полей таблицы incomes, установить поле note_inc с id='$id' значением переменной $noteVal
				
    echo "<b><span style='color:red;'>Примечание добавлено!</span></b>"; 		
  }   
	
  mysql_close();                                             // закрыть соединение с бд
}
