  */
  // при нажатии на нопку 'добавить' данные формы сохраняются в переменные:
  $noteVal = strip_tags(trim($_POST['noteField']));          // $_POST - глобальный массив, ввод в переменные с обработкой
	
  if($noteVal!=null)                                         // если поле 'примечание' не пустое 
